package rxh.hb.jsonbean;

public class HistoricalRepaymentLVBean {

	private String time;
	private String repayment_amount;
	private String repayment_of_principal;
	private String investment_number;

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getRepayment_amount() {
		return repayment_amount;
	}

	public void setRepayment_amount(String repayment_amount) {
		this.repayment_amount = repayment_amount;
	}

	public String getRepayment_of_principal() {
		return repayment_of_principal;
	}

	public void setRepayment_of_principal(String repayment_of_principal) {
		this.repayment_of_principal = repayment_of_principal;
	}

	public String getInvestment_number() {
		return investment_number;
	}

	public void setInvestment_number(String investment_number) {
		this.investment_number = investment_number;
	}

}
